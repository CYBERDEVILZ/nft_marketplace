var addressContract = "0x584bA76954256a59d751256d3a3401D23429272a";
var walletAddress = null;


console.log("contract deployed at: "+addressContract)

// script to access metamask wallet
access = async () => {
    window.web3 = new Web3(window.ethereum);
    await window.web3.eth.getAccounts().then((success) => {walletAddress = success[0]; console.log(`Wallet address: ${success[0]}`)}, (failure) => {
		walletAddress = null;}).catch((e)=>{walletAddress = null;});
}

// minting fee
mintingFee = async () => {
    await access();
	if (walletAddress == null || walletAddress == undefined){
		window.location="/connectWallet"
	}
    await window.web3.eth.sendTransaction({
        from: walletAddress,
        to: addressContract,
        value: 1000000000000000
    }).then(async(success)=>{
        console.log(success);
        await mintTokens();
    },
    (failure)=>{console.log(failure);
        window.location = "/nftbooth";
    }).catch((e)=>{
        console.log(e.message);
        window.location = "/nftbooth"
    })
}

// mint tokens
mintTokens = async (metadataURI, name, price, imageuri) => {
	var priceinEth = price*10**18;
	priceinEth = priceinEth.toString()
	await access();
    var contractInstance = await new web3.eth.Contract(abi, addressContract);
    await contractInstance.methods.mintNFT(metadataURI, name, priceinEth, imageuri).send({from: walletAddress, value:1000000000000000})
    .then((success)=>{alert("transaction success!");
window.location="/nftbooth"}, (failed)=>{console.log("failed"); window.location="/nftbooth"}).catch((e)=>{console.log(e.message); window.location="/nftbooth"});
}

// buy nfts
buyNFT = async (tokenId) => {
	await access();
	var contractInstance = await new web3.eth.Contract(abi, addressContract);
	var price = null;
	await contractInstance.methods.getNFTdata(tokenId).call({from: walletAddress}).
	then((success) => {
		price = success["price"];
	}, 
	(failure) => {
		alert("something went wrong!");
		window.location="/nftbooth/viewnft/"+tokenId;
	}).catch((e)=>console.log(e));
	await contractInstance.methods.buy(tokenId).send({from: walletAddress, value: price}).
	then((success) =>{
		alert("transaction success"); window.location="/nftbooth/"+tokenId
	},
	(failed) => {
		alert("transaction failed");  window.location="/nftbooth/"+tokenId
	})
	.catch((e)=>{
		console.log(e.message); window.location="/nftbooth/"+tokenId
	});
}

