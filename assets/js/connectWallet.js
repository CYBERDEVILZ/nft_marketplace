async function connectWallet(){
    // check if ethereum wallet is installed
    if(typeof window.ethereum !== "undefined"){

        // choose an account from wallet to connect
        await ethereum.request({method: "eth_requestAccounts"}).then(
                // success
                function(value){
                    signerAddress = value[0]; 
                    console.log(`Your wallet address: ${signerAddress}`);
                }, 
                // error
                function(error){
                    console.log(error);
                }
            );
        provider = new ethers.providers.Web3Provider(window.ethereum); // connect to the blockchain network from metamask
        connectWalletButton.innerHTML="WALLET CONNECTED!";
    }
}

